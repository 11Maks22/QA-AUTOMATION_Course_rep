// Числові змінні
const num_1 = 18;
const num_2 = 22;

// Рядковые змінні
const str_1 = "18";
const str_2 = "22";

// Булеві змінні
const bool_1 = true;
const bool_2 = false;

console.log("Додавання чисел:", num_1 + num_2);       // 40
console.log("Віднімання чисел:", num_1 - num_2);     // -4
console.log("Множення чисел:", num_1 * num_2);       // 396
console.log("Ділення чисел:", num_1 / num_2);       // 2
console.log("Додавання рядків:", str_1 + str_2);   // 1822
console.log("Додавання булевих (як чисел):", bool_1 + bool_2); // 1
console.log("Віднімання булевих (як чисел):", bool_1 - bool_2); // 1
console.log("Сума числа та рядка:", num_1 + str_1);   // 1818
console.log("Сума числа та булевого:", num_1 + bool_1); // 19

